/// 맵 노드의 종류를 분류한다.
///
/// SPECS.md §8 맵 & 노드 시스템 참조.
/// 각 값은 플레이어가 해당 노드에 도착했을 때 실행될 컨텐츠를 결정한다.
enum NodeType {
  /// ⚔️ 일반 몬스터와 전투. 처치 시 보상(카드·골드·HP 중 선택).
  monster,

  /// 💀 강화된 몬스터. 처치 시 추가 보상(유물 포함).
  elite,

  /// 👑 챕터 마지막 노드. 처치 시 런 클리어 또는 다음 챕터 진행.
  boss,

  /// 🔥 휴식(HP 회복) 또는 강화(카드 1장 업그레이드) 중 선택.
  rest,

  /// 🛒 골드로 카드 구매, 카드 제거, 유물 구매 가능.
  shop,

  /// 🏺 해금된 유물 풀에서 1개 무료 획득.
  treasure,

  /// ❓ 무작위 텍스트 이벤트. 선택지에 따라 이득 또는 손해.
  event,
}
